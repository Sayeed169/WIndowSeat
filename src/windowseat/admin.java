    /**
     * Takes id and password from user and give access to the information of database
     * If id and password do not match with database's id and password give message
     */
    private void adminLogin() throws SQLException{
        con = DB.connect();
        pst = con.prepareStatement("select * from admin");
        rs = pst.executeQuery();
        if(user.getText().equals(rs.getString("user")) && pass.getText().equals(rs.getString("password"))){
            display.setText("");
            setTable();
            t=true;
        }
        else{
            display.setText("Wrong ID or Password");
        }
    }
    
    /**
     * Gets all the information from database and sets it in the table
     * @throws SQLException 
     */
    private void setTable() throws SQLException{
        con = DB.connect();
        pst = con.prepareStatement("select * from rideinfo");
        rs = pst.executeQuery();
        
        DefaultTableModel table = (DefaultTableModel) jtable.getModel();
        
        while(table.getRowCount()>0){
            for(int a=0; a<table.getRowCount(); a++){
                table.removeRow(a);
            }
        }
        
        while(rs.next()){
            table.addRow(new Object []{Integer.parseInt(rs.getString("id")),rs.getString("name"),rs.getString("start"),rs.getString("end"),rs.getString("time"),Double.parseDouble(rs.getString("price")),rs.getString("seat"),});
        }
    }
    
