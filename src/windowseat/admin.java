    /**
     * Add new row of data in database
     * @param evt 
     */
    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        if(t){
            try{
                pst = con.prepareStatement("insert into rideinfo (name, start, end, time, price, seat) values (?,?,?,?,?,?)");
                pst.setString(1, name.getText());
                pst.setString(2, from.getText());
                pst.setString(3, to.getText());
                pst.setString(4, time.getText());
                pst.setDouble(5, Double.parseDouble(price.getText()));
                pst.setString(6, seat.getText());
                pst.execute();
                
                message.setForeground(Color.blue);
                message.setText("Record Was Added Successfully");
                setTable();
            }catch (SQLException e) {
                message.setForeground(Color.red);
                message.setText("Record Was  not Added");
                JOptionPane.showMessageDialog(null,e);
            }
        }
    }//GEN-LAST:event_addActionPerformed

    /**
     * Edit data from database and sets the changes in database
     * @param evt 
     */
    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        if(t){
            try {
                pst = con.prepareStatement("update rideinfo set id="+Integer.parseInt(i.getText())+", name='"+name.getText()+"', start='"+
                        from.getText()+"', end='"+to.getText()+"', time='"+time.getText()+"', price="+
                        Double.parseDouble(price.getText())+", seat='"+seat.getText()+"' where id="+id);
                pst.execute();
                
                message.setForeground(Color.blue);
                message.setText("Record Was Edited Successfully");
                setTable();
            } catch (SQLException e) {
                message.setForeground(Color.red);
                message.setText("Record Was  not Edited");
                JOptionPane.showMessageDialog(null,e);
            }
        }
    }//GEN-LAST:event_editActionPerformed

