    /**
     * Sets user's Start and Destination point
     * Shows option according to user's input in window
     * @param evt 
     */
    private void scarchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scarchActionPerformed
        try{
            pst = con.prepareStatement("select * from rideinfo");
            rs = pst.executeQuery();

            String s = start.getSelectedItem().toString();
            String t = end.getSelectedItem().toString();
            boolean val = true;
            
            DefaultTableModel table = (DefaultTableModel) result.getModel();
            
            while(table.getRowCount()>0){
                for(int i=0;i<table.getRowCount(); i++){
                    table.removeRow(i);
                }
            }
            while(rs.next()){
                if(rs.getString("start").equalsIgnoreCase(s) && rs.getString("end").equalsIgnoreCase(t)){
                    table.addRow(new Object[]{rs.getString("id"),rs.getString("name").toUpperCase(),rs.getString("time"),rs.getString("price")});
                    val = false;
                }
            }
            if(val){
                JOptionPane.showMessageDialog(null,"No Result Found On The Search");
            }
            rs.close();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_scarchActionPerformed

