    /**
     * Set connection with database for latest update
     * Set's traveling option for user in combo box
     * @throws SQLException 
     */
    public void make_list() throws SQLException{
        pst = con.prepareStatement("select * from rideinfo");
        rs = pst.executeQuery();
        
        ArrayList<String> f = new ArrayList();
        ArrayList<String> t = new ArrayList();
        
        while(rs.next()){
            if(!f.contains(rs.getString("start")))
                f.add(rs.getString("start"));
                
            if(!t.contains(rs.getString("end")))
                t.add(rs.getString("end"));
        }
        
        rs.close();
        
        for(int i=0; i<Math.max(f.size(), t.size());i++){
            if(i<f.size())
                start.addItem(f.get(i).toUpperCase());
            if(i<t.size())
                end.addItem(t.get(i).toUpperCase());
        }
    }
